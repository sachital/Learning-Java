
public class Assignment {
    public static void main(String[] args) {

        int a;
        int b=5;
        a=b;
        a+=b; // is same as a=a+b;
        System.out.println(a);
        a-=b; // is same as a=a-b;
        System.out.println(a);
        a*=b; // is same as a=a*b;
        System.out.println(a);
        a/=b; // is same as a=a/b;
        System.out.println(a);
        a%=b; // is same as a=a%b;
        System.out.println(a);
    }
}
